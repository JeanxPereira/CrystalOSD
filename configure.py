def clean():
    if os.path.exists(".splache"):
        os.remove(".splache")
    shutil.rmtree("asm", ignore_errors=True)
    shutil.rmtree("assets", ignore_errors=True)
    shutil.rmtree("build", ignore_errors=True)


def fix_linker_script(ld_path: Path):
    """
    Splat places `module_storage` inside `.main` data section, which pushes
    .sdata/.sbss far outside ±32KB of $gp (0x377970), breaking GPREL16 relocs.

    Original layout:
        .main 0x200000 : text + data + rodata + sdata
        .main_bss (NOLOAD) : sbss + bss
        .main_modstor 0x50A780 : module_storage

    Move module_storage to its own section after .main_bss.
    """
    text = ld_path.read_text()
    needle = "        build/asm/data/module_storage.s.o(.module_storage);\n"
    if needle not in text:
        return
    text = text.replace(needle, "")

    closing_main_bss = "        main_BSS_SIZE = ABSOLUTE(main_BSS_END - main_BSS_START);\n    }\n"
    insertion = (
        "        main_BSS_SIZE = ABSOLUTE(main_BSS_END - main_BSS_START);\n"
        "    }\n\n"
        "    main_modstor_VRAM = 0x50A780;\n"
        "    .main_modstor 0x50A780 : SUBALIGN(4)\n"
        "    {\n"
        "        FILL(0x00000000);\n"
        "        main_MODSTOR_START = .;\n"
        "        build/asm/data/module_storage.s.o(.module_storage);\n"
        "        main_MODSTOR_END = .;\n"
        "        main_MODSTOR_SIZE = ABSOLUTE(main_MODSTOR_END - main_MODSTOR_START);\n"
        "    }\n"
    )
    text = text.replace(closing_main_bss, insertion, 1)
    text = text.replace(". = ALIGN(., 16);", ". = ALIGN(., 4);")
    ld_path.write_text(text)
    print(f"Patched {ld_path}: moved module_storage to .main_modstor at 0x50A780 and fixed alignments")


def fix_bss_section_attrs(asm_dir: Path):
    """
    With ld_bss_is_noload=False, splat materializes bss as `.float 0`/`.word 0`
    directives. GAS rejects `.float` in `.bss` (hardcoded NOBITS in MIPS GAS),
    even when the section is declared `@progbits`.

    - Mark sections as PROGBITS so `.word` is accepted.
    - Replace `.float 0` with byte-equivalent `.word 0x00000000` since GAS
      refuses floats in `.bss` regardless of section flags.
    """
    section_repl = {
        '.section .bss, "wa"': '.section .bss, "aw", @progbits',
        '.section .sbss, "wa"': '.section .sbss, "aw", @progbits',
    }
    fixed_sections = 0
    fixed_floats = 0
    for s_file in asm_dir.rglob("*.s"):
        text = s_file.read_text()
        new = text
        for old, repl in section_repl.items():
            if old in new:
                new = new.replace(old, repl)
                fixed_sections += 1
        if "/* Generated by spimdisasm" in new and ".bss" in new:
            float_count = new.count(" .float 0\n")
            if float_count:
                new = new.replace(" .float 0\n", " .word 0x00000000\n")
                fixed_floats += float_count
        if new != text:
            s_file.write_text(new)
    print(
        f"Patched bss/sbss attrs in {fixed_sections} file(s); "
        f"replaced {fixed_floats} `.float 0` with `.word 0x00000000`"
    )


